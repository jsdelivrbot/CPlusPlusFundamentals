#include <iostream>

int main() {
    signed char millennium = 2;
    short centuries = 20;
    int years = 2000;
    unsigned int days = 730480;
    unsigned long hours = 17531520;

    std::cout << (int)millennium << " millennium" << std::endl;
    std::cout << centuries << " centuries" << std::endl;
    std::cout << years << " years" << std::endl;
    std::cout << days << " days" << std::endl;
    std::cout << hours << " hours" << std::endl;

    return 0;
}
